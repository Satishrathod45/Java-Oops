package typecasting;

public class wideningExample {

	public static void main(String[] args)
	{
		//int to double
		int a=6;
		double b=a;
		System.out.println("converting int "+ a+" to double is: "+b);
		
		//byte to long
		long r=100000000;
		float s=r;
		System.out.println("converting long  "+r+" into float is: "+s);
		

	}

}
